public class example_43 {
    public static void main(String[] args) {
        double three = 3.0;
        double x = 1 / three;
        double y = 4 / three;
        double z = 5 / three;
        if (x + y == z) {
            System.out.println("1/3 + 4/3 = 5/3");
        } else {
            System.out.println("1/3 + 4/3 != 5/3");
        }
    }
}